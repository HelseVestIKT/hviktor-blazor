#!/usr/bin/env node
/**
 * Generates index.html and 404.html from a shared template.
 *
 * Template source: wwwroot/entry.template.html
 * Outputs: wwwroot/index.html, wwwroot/404.html
 *
 * Placeholder tokens in the template:
 *   {{SUPPRESS_COMMENT}} - IDE suppression comment inserted only in index.html
 *
 * Can be imported as a module or run directly via `node generate-html-entries.mjs`.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const wwwroot = join(__dirname, "wwwroot");
const templatePath = join(wwwroot, "entry.template.html");

const GENERATED_NOTICE =
  "<!-- Generated by generate-html-entries.mjs - do not edit directly, edit entry.template.html instead -->\n";

// Number of leading path segments to preserve when redirecting via 404.html.
// Use 0 for custom domains or apex hosting (e.g. https://example.com/).
// Use 1 for GitHub Pages project sites (e.g. https://user.github.io/repo/).
const PATH_SEGMENTS_TO_KEEP = 0;

// Injected into 404.html. Captures the requested deep-link path and redirects
// to the site root with the path encoded into the query string, so that
// index.html can restore it via history.replaceState. See:
// https://github.com/rafgraph/spa-github-pages
const SPA_REDIRECT_SCRIPT = `<!-- Single Page Apps for GitHub Pages - https://github.com/rafgraph/spa-github-pages -->
    <script>
      (function () {
        const pathSegmentsToKeep = ${PATH_SEGMENTS_TO_KEEP};
        let l = window.location;
        l.replace(
          l.protocol + "//" + l.hostname + (l.port ? ":" + l.port : "") +
          l.pathname.split("/").slice(0, 1 + pathSegmentsToKeep).join("/") + "/?/" +
          l.pathname.slice(1).split("/").slice(pathSegmentsToKeep).join("/").replace(/&/g, "~and~") +
          (l.search ? "&" + l.search.slice(1).replace(/&/g, "~and~") : "") +
          l.hash
        );
      })();
    </script>`;

// Injected into index.html. Restores the original deep-link path that was
// encoded by the 404.html redirect script, before Blazor's router boots.
const SPA_RESTORE_SCRIPT = `<!-- Single Page Apps for GitHub Pages - https://github.com/rafgraph/spa-github-pages -->
    <script>
      (function (l) {
        if (l.search[1] === "/") {
          var decoded = l.search.slice(1).split("&").map(function (s) {
            return s.replace(/~and~/g, "&");
          }).join("?");
          window.history.replaceState(null, null,
            l.pathname.slice(0, -1) + decoded + l.hash
          );
        }
      })(window.location);
    </script>`;

const entries = [
  {
    output: "index.html",
    vars: {
      // IDE suppression hint for unresolved HtmlUnknownTarget warnings
      SUPPRESS_COMMENT: "<!--suppress HtmlUnknownTarget-->\n",
      HEAD_SCRIPT: SPA_RESTORE_SCRIPT,
    },
  },
  {
    output: "404.html",
    vars: {
      SUPPRESS_COMMENT: "",
      HEAD_SCRIPT: SPA_REDIRECT_SCRIPT,
    },
  },
];

/**
 * Renders the shared HTML template with the given placeholder values.
 *
 * @param {string} template - Raw template string.
 * @param {Record<string, string>} vars - Placeholder values keyed by token name.
 * @returns {string} Rendered HTML with the generated-file notice injected.
 */
function render(template, vars) {
  const rendered = template.replace(/{{(\w+)}}/g, (_, key) => vars[key] ?? "");
  // Insert the generated notice after the doctype declaration
  return rendered.replace(/^(<!doctype html>\n)/i, `$1${GENERATED_NOTICE}`);
}

/**
 * Generates index.html and 404.html from the shared entry template.
 */
export function generateHtmlEntries() {
  const template = readFileSync(templatePath, "utf8");

  for (const { output, vars } of entries) {
    const outPath = join(wwwroot, output);
    writeFileSync(outPath, render(template, vars), "utf8");
    console.log(`Generated ${output}`);
  }
}

// Run directly when executed as a CLI script
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  try {
    generateHtmlEntries();
  } catch (error) {
    console.error(`HTML generation failed: ${error.message}`);
    process.exit(1);
  }
}
