#!/usr/bin/env node
/**
 * Generates index.html and 404.html from a shared template.
 *
 * Template source: wwwroot/entry.template.html
 * Outputs: wwwroot/index.html, wwwroot/404.html
 *
 * Placeholder tokens in the template:
 *   {{SUPPRESS_COMMENT}} - IDE suppression comment inserted only in index.html
 *
 * Can be imported as a module or run directly via `node generate-html-entries.mjs`.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const wwwroot = join(__dirname, "wwwroot");
const templatePath = join(wwwroot, "entry.template.html");

const GENERATED_NOTICE =
  "<!-- Generated by generate-html-entries.mjs - do not edit directly, edit entry.template.html instead -->\n";

const entries = [
  {
    output: "index.html",
    vars: {
      // IDE suppression hint for unresolved HtmlUnknownTarget warnings
      SUPPRESS_COMMENT: "<!--suppress HtmlUnknownTarget-->\n",
    },
  },
  {
    output: "404.html",
    vars: {
      SUPPRESS_COMMENT: "",
    },
  },
];

/**
 * Renders the shared HTML template with the given placeholder values.
 *
 * @param {string} template - Raw template string.
 * @param {Record<string, string>} vars - Placeholder values keyed by token name.
 * @returns {string} Rendered HTML with the generated-file notice injected.
 */
function render(template, vars) {
  const rendered = template.replace(/{{(\w+)}}/g, (_, key) => vars[key] ?? "");
  // Insert the generated notice after the doctype declaration
  return rendered.replace(/^(<!doctype html>\n)/i, `$1${GENERATED_NOTICE}`);
}

/**
 * Generates index.html and 404.html from the shared entry template.
 */
export function generateHtmlEntries() {
  const template = readFileSync(templatePath, "utf8");

  for (const { output, vars } of entries) {
    const outPath = join(wwwroot, output);
    writeFileSync(outPath, render(template, vars), "utf8");
    console.log(`Generated ${output}`);
  }
}

// Run directly when executed as a CLI script
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  try {
    generateHtmlEntries();
  } catch (error) {
    console.error(`HTML generation failed: ${error.message}`);
    process.exit(1);
  }
}
