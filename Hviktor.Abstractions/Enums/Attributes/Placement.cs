namespace Hviktor.Abstractions.Enums.Attributes;

/// <summary>
/// Represents the position of an element in a layout.
/// </summary>
public enum Placement
{
    /// <summary>
    /// Represents the center position of an element.
    /// </summary>
    Center,

    /// <summary>
    /// Represents the top position of an element.
    /// </summary>
    Top,

    /// <summary>
    /// Represents the top-start corner position of an element.
    /// </summary>
    TopStart,

    /// <summary>
    /// Represents the top-end corner position of an element.
    /// </summary>
    TopEnd,

    /// <summary>
    /// Represents the bottom position of an element.
    /// </summary>
    Bottom,

    /// <summary>
    /// Represents the bottom-start corner position of an element.
    /// </summary>
    BottomStart,

    /// <summary>
    /// Represents the bottom-end corner position of an element.
    /// </summary>
    BottomEnd,

    /// <summary>
    /// Represents the left position of an element.
    /// </summary>
    Left,

    /// <summary>
    /// Represents the left-start corner position of an element.
    /// </summary>
    LeftStart,

    /// <summary>
    /// Represents the left-end corner position of an element.
    /// </summary>
    LeftEnd,

    /// <summary>
    /// Represents the right position of an element.
    /// </summary>
    Right,

    /// <summary>
    /// Represents the right-start corner position of an element.
    /// </summary>
    RightStart,

    /// <summary>
    /// Represents the right-end corner position of an element.
    /// </summary>
    RightEnd,
}