namespace Hviktor.Abstractions.Enums.Attributes;

/// <summary>
/// Represents the position of an element in a layout.
/// </summary>
public enum Position
{
    /// <summary>
    /// Represents the start position of an element.
    /// </summary>
    Start,

    /// <summary>
    /// Represents the end position of an element.
    /// </summary>
    End,

    /// <summary>
    /// Represents the top position of an element.
    /// </summary>
    Top,

    /// <summary>
    /// Represents the bottom position of an element.
    /// </summary>
    Bottom,

    /// <summary>
    /// Represents the left position of an element.
    /// </summary>
    Left,

    /// <summary>
    /// Represents the right position of an element.
    /// </summary>
    Right,

    /// <summary>
    /// Represents the center position of an element.
    /// </summary>
    Center,

    /// <summary>
    /// Represents the top-left corner position of an element.
    /// </summary>
    TopLeft,

    /// <summary>
    /// Represents the top-right corner position of an element.
    /// </summary>
    TopRight,

    /// <summary>
    /// Represents the bottom-left corner position of an element.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// Represents the bottom-right corner position of an element.
    /// </summary>
    BottomRight
}