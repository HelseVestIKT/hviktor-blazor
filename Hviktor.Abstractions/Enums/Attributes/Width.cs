namespace Hviktor.Abstractions.Enums.Attributes;

/// <summary>
/// Width variants for layout components.
/// </summary>
public enum Width
{
    /// <summary>
    /// Full width.
    /// </summary>
    /// <remarks>The width is 100% of the parent container.</remarks>
    Full,

    /// <summary>
    /// Auto width.
    /// </summary>
    /// <remarks>The width is determined by the content.</remarks>
    Auto
}